
//unary operator example java program
class Test2010222 {
public static void main(String[] args) {
int x=25;
//postfic
System.out.println(x); //25
System.out.println(x--);//25
System.out.println(x);//24
// prefix 
System.out.println(x); //24
System.out.println(++x);//25
System.out.println(--x);//24
}
}
